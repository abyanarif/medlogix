        // Micro-interactions and subtle effects
        document.addEventListener('DOMContentLoaded', () => {
            // Section entrance animations
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('opacity-100', 'translate-y-0');
                        entry.target.classList.remove('opacity-0', 'translate-y-10');
                    }
                });
            }, { threshold: 0.1 });

            document.querySelectorAll('section').forEach(section => {
                // Skip adding initial opacity/transform animations to hero section to avoid double animations
                if (section.id !== 'home') {
                    section.classList.add('transition-all', 'duration-700', 'opacity-0', 'translate-y-10');
                    observer.observe(section);
                }
            });

            // Smooth scroll offset & active-nav click behavior
            const navLinks = document.querySelectorAll('.nav-link');
            let isScrollingTo = false;
            let scrollTimeout;

            navLinks.forEach(link => {
                link.addEventListener('click', (e) => {
                    const targetId = link.getAttribute('href');
                    if (targetId.startsWith('#')) {
                        e.preventDefault();
                        const targetElement = document.querySelector(targetId);
                        if (targetElement) {
                            isScrollingTo = true;
                            
                            // Immediately update active class
                            navLinks.forEach(l => l.classList.remove('active-nav'));
                            link.classList.add('active-nav');

                            window.scrollTo({
                                top: targetElement.offsetTop - 80, // Adjust for navbar height
                                behavior: 'smooth'
                            });

                            // Re-enable scrollspy updates after scroll finishes
                            clearTimeout(scrollTimeout);
                            scrollTimeout = setTimeout(() => {
                                isScrollingTo = false;
                            }, 800);
                        }
                    }
                });
            });

            // ScrollSpy scroll-listener for robust tracking
            const handleScrollSpy = () => {
                if (isScrollingTo) return;

                const sections = document.querySelectorAll('section[id]');
                const navLinks = document.querySelectorAll('.nav-link');
                
                // Edge case: top of the page
                if (window.scrollY < 100) {
                    navLinks.forEach(l => l.classList.remove('active-nav'));
                    const homeLink = document.querySelector('.nav-link[href="#home"]');
                    if (homeLink) homeLink.classList.add('active-nav');
                    return;
                }

                // Edge case: bottom of the page
                if ((window.innerHeight + window.scrollY) >= document.body.offsetHeight - 10) {
                    navLinks.forEach(l => l.classList.remove('active-nav'));
                    const lastLink = navLinks[navLinks.length - 1];
                    if (lastLink) lastLink.classList.add('active-nav');
                    return;
                }

                let current = '';
                sections.forEach(section => {
                    const sectionTop = section.offsetTop;
                    // Trigger section active state slightly before reaching it
                    if (window.scrollY >= (sectionTop - 120)) {
                        current = section.getAttribute('id');
                    }
                });

                if (current) {
                    navLinks.forEach(link => {
                        if (link.getAttribute('href') === `#${current}`) {
                            if (!link.classList.contains('active-nav')) {
                                navLinks.forEach(l => l.classList.remove('active-nav'));
                                link.classList.add('active-nav');
                            }
                        }
                    });
                }
            };

            window.addEventListener('scroll', handleScrollSpy);
            // Run initially to set active state on load
            handleScrollSpy();
        });
    </script>


